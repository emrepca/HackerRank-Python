N,M = map(int, input().split(" "))
#N: rows
#M: columns

for i in range(N//2):
	middle = ".|." * (2 * i + 1)
	print(middle.center(M,"-"))

print("WELCOME".center(M,"-"))

for i in range(N//2,0,-1):
	middle = ".|." * (2 * i - 1)
	print(middle.center(M,"-"))

